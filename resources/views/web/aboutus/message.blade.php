                <div class="message_box">
                    <h4><span>私の原風景</span></h4>
                    <p class="txt">私が幼少の頃、家の敷地に小さなプレハブが建っていました。毎日賑やかにパートの女性たちが自転車で通ってきて、そのプレハブ小屋で、何かをつくっているようでした。ときどき、手伝うことがありました。ハンダのにおい、電子部品の手触りを、今でも鮮明に覚えています。時は1980 年頃、これが私の物作りとの出会い、原風景でした。<br><br>

                        いつしか、家業が製造業と言われるものであることを知り、それが下請けと呼ばれる業態であることを知ったのは、20 才の頃でした。この頃には、日本の製造業は人件費の安い海外でつくることが常道とされる時代になり、折しもバブル崩壊直後、下請けという業態がますます厳しくなる時代が始まっていたことに、ぼんやりと気づいていました。<br><br>

                        その後、私は技術系の会社に入り、いくつかの海外事業に携わっていました。その中で体験してきたのは、日本の名だたるメーカーが年を追うごとに市場撤退、買収される厳しい競争環境でした。目の前で、自分が情熱を注ぎ、専門性を発揮し、日々の生活の糧を得ていた技術が、どんどん安価な製品や技術に置き換えられてゆく・・。これほど悔しく、寂しいことはありません。結局、私が携わっていた技術や職務は、安価な労働力、安価な技術に置き換えられてしまい、いまは存在していません。<br>
                        いわゆる、いまで言う「将来消えてなくなる職業」を体験したのです。
                        </p>
                </div>

                <div class="message_box">
                    <h4><span>日本で事業を継ぐ意味</span></h4>
                    <p class="txt">時は過ぎ、私が東海電子を継ぐべく入社したのは、2001 年でした。私は、将来消えてなくなる家業を継ぐのか？　目の前で見てきたことを自社で体現するのか？　いや違います。決意していたことはひとつ。</p>

                    <h5>ものづくり企業として、新しい市場に進出すること。</h5>
                    <p class="txt">私は、日本の製造業の勃興を庭先でみてきた者として、海外の新興国の短期間でのめざましい成長を目の当たりにした者として、この時代、この日本という国で、製造業という業を継ぎ、永続を目指す意味は何なのだろうか？日本の産業史における、事業を持つ家に生まれた者の使命は何なのだろうか？いつもこの問いがあります。私は、新しい製品や、海外という新しい市場へ進出するという強い思いをもって、東海電子に参画しました。</p>

                    <h5>挑戦なくして、未来なし。</h5>
                    <p class="txt">「飲酒運転防止事業」は、東海電子にとってはじめての新事業でした。<br>
                        先代の思い、初期を支えた当時の社員たち、この当時ジョインした意気あるメンバーの情熱があり、幸いなことにはじめの1 機種・1 台・1 社は、いまや10 機種・10 万台・2 万企業という規模に至っています。<br><br>

                        これは、小さい企業ながらも新しいことに積極的に挑戦する社員たちの情熱によるもの、そして何より、安全な交通社会を実現するために安全に投資しようというお客様の交通社会への真剣な思いによるものです。<br><br>

                        当社の経営理念「社会の安全、安心、健康を創造する」は、常に、お客様とともにあります。一方で、お客様であると同時に、業種は違えども、当社と同じ日本の中小企業です。経営者もおそらく私と同年代であろうと思います。運輸業も、製造業も、そしてあらゆる業種の中小企業が、事業や業界を維持・発展させるためには、「挑戦なくして、未来なし」、だということに気づいているのではないでしょうか。</p>
                </div>

                <div class="message_box">
                    <h4><span>これから</span></h4>
                    <p class="txt">人生100 年時代と言われています。100 年を途切れさせるのは、間違いなく、不慮の事故や病気です。われわれ東海電子の夢は大きく、現行の飲酒運転ゼロ事業を国外へ広げ、さらに、この20 年で培った「呼気から飲酒状態をはかる」という技術をさらに、こころやからだの変化を早期に見つけ、病気や事故を未然に防ぐ技術開発と製品開発へと広げてゆきたいと考えています。<br>
                        私は、そして、われわれ東海電子は、『社会の安全、安心、健康を創造する』という経営理念のもと、呼気で社会課題の解決を実現する世界でも唯一無二の企業を目指します。
                        </p>
