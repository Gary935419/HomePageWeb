                                カメラを使用し、PCに接続し、社員データとして管理するという業務用アルコール測定システムというコンセプトが、バス、タクシー、トラック会社に受け入れられ、わたしたちの「飲酒運転をゼロに」という長い旅がこの日から始まることになったのです。
