                    <dl class="rev history_mes_short">
                        <dt><p class="txt">1990年代後半から2000年にかけて、実は日本の飲酒運転事故死者はピークにありました。1999年11月、社会に衝撃を与える飲酒運転事故が起きました。その痛ましい飲酒運転事故は連日報道され、お酒に甘い社会の空気も一変し社会問題化しました。<br>
                                この事件の加害者は、トラック会社の社員ドライバーでした。<br>
                                当時の代表（現会長）は、一連の報道を目の当たりし、ある日「なぜ、企業が社員の飲酒運転を防げなかったのか？」と疑問を持ちました。社員を管理できるアルコール検知器が必要ではないか？　そういうものはないのか？　
                                当時、小型のアルコールチェッカーがいくつかあったようです。しかし、企業が管理するタイプの製品は市場に無いように見えたため、「だったら創ろう」と考え、開発したのが「業務用アルコール測定システム」でした。<br><br>

                                わたしたちは、始めてつくった自社製品を、「プロドライバー・プロ事業者に使って頂きたい」との思いを込めてALC-PROと名付けました。

                            </p></dt>
