                                        乗合バス、貸切バス、タクシー、トラック等　自動車運送事業者のうち、貸切バスは全車両にドライブレコーダーを装着する義務及び、ドライブレコーダーを用いた安全教育を行う義務が課せられています。<br><br>

                                        ドライブレコーダーはカーショップやカーディーラーでも気軽に入手できるようになりました。最近は、クラウドタイプや360°タイプも増えてきています。<br><br>

                                        このように、ドライブレコーダーは、自動車運送事業者だけでなく、安全運転管理者選任事業所をはじめ業務で車両を使用する企業や、個人でも普通に事故防止のために使う機器となっています。


