                            <h4 class="page_ttl_sub02"><span>国内外のアルコール検知器　技術規格</span></h4>
                            <p class="img mb30"><img src="{{ asset('assets/web_img/technology/tec_p01_9.png') }}" alt=""></p>
                            <p class="txt mb30">アルコール検知器は歴史的に、国家（主に警察行政）による飲酒運転違反者の取締りのために実用化されてきました。<br><br>

                            「体内アルコール濃度」や「呼気アルコール濃度」という生理学的な分析は、工業機器を通じて、数値化されます。その際、国家が『単位』として正式採用する場合一般的には「計量行政」と言われ、原則技術規格化・文書化され、公示されます。<br>
                            アルコール検査機器には国際規格があり、通称「R126」と呼ばれています。<br>
