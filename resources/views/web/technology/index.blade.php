                            <div class="history_mes">
                                <dl>
                                    <dt><p class="txt">日本では2010年代以降、すべての運転免許証に「非接触ＩＣチップ」が内臓されるようになりました。運転免許証取得や更新時に「暗証番号１」「暗証番号２」を使うことで、運転免許証の表面、ＩＣチップの電磁的・電子的情報が同一であることを気軽に誰でも確認できるようになりました。<br>

                                        2010年より以前は、レンタカー貸し出しや銀行での身分証明として多用されていた運転免許証ですが、偽造運転免許証による違法行為が増え。これを防ぐため、日本国では運転免許証のＩＣ化に踏み切りました。<br><br>

                                       2014年頃からバス、タクシー、トラック等の自動車運送事業者や安全運転管理選任事業所によって、ＩＣ運転免許証リーダーとアルコールチェックシステムと連動させるシステムが普及しはじめました。<br><br>

                                       今後、ＩＣ運転免許証はマイナンバーカードと一体化したり、モバイル運転免許証アプリも出てくるなど、社会のデジタル化とともに国内外で身分証明書として重要なものになってゆくでしょう。

