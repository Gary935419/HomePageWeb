                            <div class="history_mes">
                                <dl>
                                    <dt><p class="txt">日本では業務で自動車を使う企業や運輸・運送事業者等プロ事業者は、法令で『点呼』という安全確認行為が義務付けられています。<br><br>

                                            具体的には、道路交通法施行規則、旅客自動車運送事業運輸規則、貨物自動車運送事業輸送安全規則により、運転前後、または運転途中、ドライバーが疲労や酒気を帯びていないか等の確認が必要です。<br><br>

                                            2007年頃から、IT技術を活用した点呼が法令で認められるようになったことから「点呼システム」という製品市場が生まれました。<br>
                                            さらに2021年以降、事業用自動車総合安全プラン2025という国土交通省の中期安全政策方針により点呼システムやロボット点呼が制度化され、プロ事業者の運行管理の現場で多く使われるようになりました。<br>
                                            現在、点呼告示第266号により、大きくは２つに分類されています。</p>
