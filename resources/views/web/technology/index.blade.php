                                <dl class="rev">
                                    <dt>
                                        <p class="txt">人の体調や疲労は、見た目ではわかりません。特に、業務で運転を使用とする社員やプロドライバーは、会社や同僚に迷惑をかけまいと「体調不良です」とは言えず、場合によっては事故を起こすこともあります。<br><br>
                                            このため安全運転管理者や運行管理者は、日々社員やドライバーの健康状態を把握する必要があります。特に昨今は、自己申告に頼らず、システム機器を使って体調を見える化し、運行可否の材料にすることも増えてきました。<br><br>

                                            昨今、技術の進展により、デジタル血圧計とスマートフォンを連動させてバイタルデータを管理したり、すスマートウォッチを使ったバイタルデータの管理が可能になっています。<br><br>
                                            企業は、健康起因事故防止のみならず、労働安全衛生法、健康増進法、特定保健指導の観点で、事業所で日々の血圧測定を実施したり、企業が社員にスマートウォッチを支給するなど、健康を意識した事業活動が盛んになってきています。

