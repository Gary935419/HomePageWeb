                        <h3 class="page_ttl_sub"><span>予防安全テクノロジー</span></h3>
                        <p class="txt mb50">クルマは人が操作するものです。しかし、時に人は、眠くなったり、イライラしたりし、場合によっては道路交通法を無視した運転をします。クルマも長年つかっていると、故障や消耗する部品が出てきます。このように、「ひと」や「くるま」が、路上に出ても安全であることを予め担保する法的な仕組みは、安全運転管理、運行管理、整備管理と一般的に言われ、昨今ではさまざまな先進的な技術が使われることから、『予防安全テクノロジー』と総称されます。<br>点呼システム、運行管理システム、整備管理システムは、広い意味で予防安全テクノロジーのひとつです。</p>
