                        <h4 class="page_ttl_sub02"><span>アルコール検知器産業史の意義</span></h4>
                        <div class="history_mes tec_tab_bot">
                            <dl>
                                <dt><p class="txt mb30">呼気アルコール検知技術の進歩は、これまで見逃されていた飲酒運転や警察官による不正な見逃しをなくすことができる「客観指標」として、多いに交通社会に貢献してきた歴史があります。その歴史に貢献した数多くの科学者・生化学者・研究者がいました。<br>
