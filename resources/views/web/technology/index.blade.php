                        <h4 class="page_ttl_sub02"><span>血中濃度・呼気中濃度</span></h4>
                        <div class="history_mes tec_tab_bot">
                            <p class="txt mb30">通常、体内に入ったアルコールは、30分ほどで血液に取り込まれます。本来ならば、測定するためにはこれらの血液を採取する必要があります。しかしながら、血液の採取をすることなく呼気から濃度を測定するため、間接的に濃度換算する法則があります。それがヘンリーの法則です。<br><br>

                                <b>ヘンリーの法則</b><br>
                                ヘンリーの法則とは物理化学の一般法則で、簡単に言うと、液体中に溶解している物質が空気（蒸気）中にあった場合一定の正比例となる、空気（蒸気）中の物質が液体中に溶解していた場合一定の正比例となる、という基礎理論です。アルコールなどの揮発性物質が溶媒 (血液など) に溶解している場合に気体 (つまり、呼気) 中のアルコールの濃度が、体内のア​​ルコールの濃度に比例すると予測します。<br>
                                呼気検査の歴史においては、初期の頃から、ヘンリーの法則を用いて伝統的に次の濃度換算式で血中濃度と呼気中濃度を換算することが基本となっています。<br><br>

                                「血液１リッターに含まれるアルコール重量÷2100≒呼気１リッターに含まれるアルコール重量」<br><br>

                                現在、呼気を吹き込み測定を行うアルコール測定器の大半はこの法則に基づき、アルコールの濃度を測定しています。弊社もこの法則に基づき、換算を行っております。しかしながら、この法則はあくまで近似値であって、絶対値ではありません。実際には、この他にも肺機能の換気量と心臓の脈拍などによって測定値ばらつきが生じます。さらに通常はセンサの感度、使用環境による外的要因によるばらつきなども考慮しなくてはなりません。
                                </p>

