        function isValidPhoneNumber(phone) {
            try {
                var regex = /^0\d{1,4}-\d{1,4}-\d{4}$/;
                if (regex.test(phone)) {
                    return true;
                } else {
                    return false;
                }
            } catch (err) {
                return false;
            }
        }
        function isValidEmail(email) {
            try {
                var regex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
                if (regex.test(email)) {
                    return true;
                } else {
                    return false;
                }
            } catch (err) {
                return false;
            }
        }
